//インデックス番号が、まだ余り分からない方は配列の国の位置を変えて出力結果を見てみよう！
//出力される国の順番が変わります。


let numbers = ['日本', 'アメリカ', 'ロシア'];


//下記に書いてあるコードは編集しないでください。

console.log(numbers[0]);//日本と出力

console.log(numbers[1]);//アメリカと出力

console.log(numbers[2]);//ロシアと出力

//値を新しく変数numberに追加するよ！
//インデックス番号が現在「２」まで使われているから次の番号は「３」だね！

numbers[3] = 'モンゴル';
console.log(numbers[3]);

//値を更新するよ！インデックス番号２番を更新するよ！

numbers[2] = 'ペルー';
console.log(numbers[2]);